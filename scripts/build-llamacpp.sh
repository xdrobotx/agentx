#!/usr/bin/env bash

set -euo pipefail

###############################################################################
# Paths
###############################################################################

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PROJECT_ROOT="$(realpath "$SCRIPT_DIR/..")"

LLAMACPP_SRC="$PROJECT_ROOT/llama.cpp"

###############################################################################
# Config
###############################################################################

source "$PROJECT_ROOT/config/build.env"

INSTALL_DIR="$INSTALL_DIR_LINUX"

###############################################################################
# Logging
###############################################################################

log() {
    printf "\033[0;36m[agentx] %s\033[0m\n" "$*"
}

###############################################################################
# Validation
###############################################################################

if [[ ! -f "$LLAMACPP_SRC/CMakeLists.txt" ]]; then
    echo "llama.cpp source tree not found"
    exit 1
fi

mkdir -p "$INSTALL_DIR"

###############################################################################
# Submodule
###############################################################################

# log "Updating submodules..."

# git submodule update --init --recursive

###############################################################################
# Configure
###############################################################################

cd "$LLAMACPP_SRC"

log "Configuring build..."

cmake -B build \
    -DGGML_CUDA=ON \
    -DGGML_NATIVE=OFF \
    -DCMAKE_INSTALL_PREFIX="$INSTALL_DIR" \
    -DCMAKE_CUDA_ARCHITECTURES="$CUDA_ARCH"

###############################################################################
# Build
###############################################################################

log "Building..."

cmake --build build \
    --config "$BUILD_TYPE" \
    --parallel

###############################################################################
# Install
###############################################################################

log "Installing..."

cmake --install build \
    --config "$BUILD_TYPE"

###############################################################################
# Cleanup
###############################################################################

if [[ "$KEEP_BUILD_DIR" != "true" ]]; then
    log "Cleaning build directory..."
    rm -rf build
fi

log "Done."
